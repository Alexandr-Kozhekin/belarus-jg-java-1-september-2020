package instructor.lesson_1;

public class MathOperationsDemo {

    public static void main(String[] args) {

        int x = 5;
        int y = 10;
        int z = x + y + 7;

        System.out.println("x + y + 7 = " + z);
        System.out.println("x + y + 7 = " + x + y + 7);
        System.out.println("x + y + 7 = " + (x + y + 7));

    }
}
